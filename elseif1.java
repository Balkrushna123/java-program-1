public class elseif1
{
public static void main(String[] args)
{
int amount=5000;

if(amount>=5000)
{
System.out.println("5star");
}
else if(amount>=1000)
{
System.out.println("kfc");
}
else if(amount>=500)
{
System.out.println("mcad");
}
else if(amount>=100)
{
System.out.println("thali");
}
else
System.out.println("shankar mandir");
}
}
